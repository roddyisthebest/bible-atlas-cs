export function getTranslation(key: string, lang: Language = "en"): any {
  const keys = key.split(".");
  let value: any = translations[lang];

  for (const k of keys) {
    value = value?.[k];
  }

  return value || key;
}

export function useTranslation(lang: Language = "en") {
  return {
    t: (key: string) => getTranslation(key, lang),
    lang,
  };
}
